package flower;

public class Flower {
    String name;
    double price;
    int freshnessLevel;
    double stemLength;

    public Flower(String name, double price, int freshnessLevel, double stemLength) {
        this.name = name;
        this.price = price;
        this.freshnessLevel = freshnessLevel;
        this.stemLength = stemLength;
    }
}
